

var numeroUm=parseInt(prompt("escreva o primeiro numero:"));



var numeroDois=parseInt(prompt("escreva o segundo numero maior que o primeiro:"));


let c=numeroUm;

while(c<numeroDois){
    var diferenca=numeroDois-numeroUm;


    var soma=c+c;


    var media=soma/diferenca


    document.write(media);

    
    c++;
    
}